
"""
    Bucles: for

  Determinados:
    . Se ejecutan un número determinado de veces.
    . Se sabe a priori cuántas veces se va a ejecutar el código del interior de bucle.

  Indeterminados:
    . Se ejecutan un número indeterminado de veces.
    . No se sabe a priori cuántas veces se va a ejecutar el código del interior del bucle.
    . El número de veces que se ejecutará dependerá de las circunstancias durante la ejecución del programa.
"""

# La cantidad de veces que se repetirá el Bucle depende de la cantidad de valores que exista en la lista.
for i in ['Primavera', 'Verano', 'Otoño', 'Invierno']: # Declaración del Bucle
  print(i) # Cuerpo del Bucle


